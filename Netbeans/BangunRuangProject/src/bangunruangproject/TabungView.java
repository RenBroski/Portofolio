  private void refresh() {
        textRadius.setText("");
        textTinggi.setText("");
        textVolume.setText("");
        textLuas.setText("");
        textRadius.requestFocus();
    }

      private void buttonHitungAction() {
        try {
            double radius = Double.parseDouble(textRadius.getText());
            double tinggi = Double.parseDouble(textTinggi.getText());
            Tabung t = new Tabung(radius, tinggi);
            
            textVolume.setText(t.getVolume() + " cm³");
            textLuas.setText(t.getLuasPermukaan() + " cm²");
        } catch (NumberFormatException error) {
            System.err.println("Error at TabungView-buttonHitungAction, details: " + error.toString());
            JOptionPane.showMessageDialog(this, "Error: " + error.getMessage());
        }
    }

