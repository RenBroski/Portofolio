 private void buttonHitungAction() {
        try {
            double panjang = Double.parseDouble(textPanjang.getText());
            double lebar = Double.parseDouble(textLebar.getText());
            PersegiPanjang pp = new PersegiPanjang(panjang, lebar);
            
            textLuas.setText(pp.getLuas() + " cmÂ²");
            textKeliling.setText(pp.getKeliling() + " cm");
        } catch (NumberFormatException error) {
            System.err.println("Error at PersegiPanjangView-buttonHitungAction, details: " + error.toString());
            JOptionPane.showMessageDialog(this, "Input tidak valid. Silakan masukkan angka.");
        }
    }
 
     private void refresh() {
        textPanjang.setText("");
        textLebar.setText("");
        textLuas.setText("");
        textKeliling.setText("");
        textPanjang.requestFocus();
    }

