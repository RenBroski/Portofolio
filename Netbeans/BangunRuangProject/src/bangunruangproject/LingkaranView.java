    private void buttonHitungAction() {
        try {
            double radius = Double.parseDouble(textRadius.getText());
            Lingkaran l = new Lingkaran(radius);
            
            textLuas.setText(l.getLuas() + " cmÂ²");
            textKeliling.setText(l.getKeliling() + " cm");
        } catch (NumberFormatException error) {
            System.err.println("Error at LingkaranView-buttonHitungAction, details: " + error.toString());
            JOptionPane.showMessageDialog(this, "Error: " + error.getMessage());
        }
    }

    private void refresh() {
        textRadius.setText("");
        textLuas.setText("");
        textKeliling.setText("");
        textRadius.requestFocus();
    }

