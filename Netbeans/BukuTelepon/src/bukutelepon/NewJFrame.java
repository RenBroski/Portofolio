    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
// Ambil nilai dari form
    String kode = jTextFieldKode.getText();
    String nama = jTextFieldNama.getText();
    String noTelepon = jTextFieldNoTelepon.getText();
    String email = jTextFieldEmail.getText();
    String alamat = jTextFieldAlamat.getText();
    String status = comboBoxStatus.getSelectedItem().toString();

    // Validasi input
    if (kode.isEmpty() || nama.isEmpty() || noTelepon.isEmpty() || email.isEmpty() || alamat.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua kolom harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        java.sql.Connection conn = (java.sql.Connection) koneksi.koneksiDB();

        // Periksa apakah data dengan kode ini sudah ada di database
        String checkSql = "SELECT * FROM daftar WHERE Kode = ?";
        java.sql.PreparedStatement checkStmt = conn.prepareStatement(checkSql);
        checkStmt.setString(1, kode);
        java.sql.ResultSet rs = checkStmt.executeQuery();

        if (rs.next()) {
            // Jika data dengan kode ini sudah ada, lakukan update (EDIT)
            String updateSql = "UPDATE daftar SET Nama = ?, NoTelepon = ?, Email = ?, Alamat = ?, Status = ? WHERE Kode = ?";
            java.sql.PreparedStatement updateStmt = conn.prepareStatement(updateSql);
            updateStmt.setString(1, nama);
            updateStmt.setString(2, noTelepon);
            updateStmt.setString(3, email);
            updateStmt.setString(4, alamat);
            updateStmt.setString(5, status);
            updateStmt.setString(6, kode);

            updateStmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diperbarui!");
        } else {
            // Jika data dengan kode ini belum ada, lakukan insert (SAVE)
            String insertSql = "INSERT INTO daftar (Kode, Nama, NoTelepon, Email, Alamat, Status) VALUES (?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement insertStmt = conn.prepareStatement(insertSql);
            insertStmt.setString(1, kode);
            insertStmt.setString(2, nama);
            insertStmt.setString(3, noTelepon);
            insertStmt.setString(4, email);
            insertStmt.setString(5, alamat);
            insertStmt.setString(6, status);

            insertStmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data baru berhasil disimpan!");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }

    // Perbarui JTable
    tampilkandata();

    // Bersihkan form
    jTextFieldKode.setText("");
    jTextFieldNama.setText("");
    jTextFieldNoTelepon.setText("");
    jTextFieldEmail.setText("");
    jTextFieldAlamat.setText("");
    comboBoxStatus.setSelectedIndex(0);

    // Aktifkan kembali "Kode" untuk input baru
    jTextFieldKode.setEditable(true);
    }//GEN-LAST:event_SimpanActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
         int selectedRow = jTable2.getSelectedRow();

    // Periksa apakah ada baris yang dipilih
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus dari tabel!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Ambil nilai "Kode" dari kolom pertama pada baris yang dipilih
    String kode = jTable2.getValueAt(selectedRow, 0).toString();

    // Konfirmasi penghapusan
    int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data dengan Kode: " + kode + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        // Hapus data dari database
        String sql = "DELETE FROM daftar WHERE Kode = ?";
        java.sql.Connection conn = (java.sql.Connection) koneksi.koneksiDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, kode);

        int rowsAffected = pst.executeUpdate(); // Menangkap jumlah baris yang dihapus
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan di database!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }

    // Perbarui data di JTable
    tampilkandata();
    }//GEN-LAST:event_DeleteActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
     // Periksa apakah ada baris yang dipilih di JTable
    int selectedRow = jTable2.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Pilih data yang ingin diedit dari tabel!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Ambil nilai dari baris yang dipilih di JTable
    String kode = jTable2.getValueAt(selectedRow, 0).toString();
    String nama = jTable2.getValueAt(selectedRow, 1).toString();
    String noTelepon = jTable2.getValueAt(selectedRow, 2).toString();
    String email = jTable2.getValueAt(selectedRow, 3).toString();
    String alamat = jTable2.getValueAt(selectedRow, 4).toString();
    String status = jTable2.getValueAt(selectedRow, 5).toString();

    // Isi data ke form
    jTextFieldKode.setText(kode);
    jTextFieldNama.setText(nama);
    jTextFieldNoTelepon.setText(noTelepon);
    jTextFieldEmail.setText(email);
    jTextFieldAlamat.setText(alamat);
    comboBoxStatus.setSelectedItem(status);

    // Pastikan "Kode" tidak bisa diedit
    jTextFieldKode.setEditable(false);
    
    // Tampilkan pesan untuk memberi tahu pengguna bahwa data siap diedit
    JOptionPane.showMessageDialog(null, "Data telah diambil. Silakan lakukan perubahan pada form, lalu klik tombol 'Simpan' untuk memperbarui data.");

    }//GEN-LAST:event_EditActionPerformed

