    private void TampilActionPerformed(java.awt.event.ActionEvent evt) {                                         
    try {
        // Mendapatkan baris yang dipilih dari JTable
        int row = jTable2.getSelectedRow();
        String tabelKlik = jTable2.getModel().getValueAt(row, 0).toString();

        // Membuat koneksi ke database
        java.sql.Connection conn = (java.sql.Connection) koneksi.koneksiDB();

        // SQL untuk mendapatkan data berdasarkan Kode
        String sql = "SELECT * FROM daftar WHERE Kode = '" + tabelKlik + "'";

        // Eksekusi query
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet sqlResult = stm.executeQuery(sql);

        // Menampilkan data ke form jika ditemukan
        if (sqlResult.next()) {
            String add1 = sqlResult.getString("Kode");
            jTextFieldKode.setText(add1);

            String add2 = sqlResult.getString("Nama");
            jTextFieldNama.setText(add2);

            String add3 = sqlResult.getString("NoTelepon");
            jTextFieldNoTelepon.setText(add3);

            String add4 = sqlResult.getString("Email");
            jTextFieldEmail.setText(add4);

            String add5 = sqlResult.getString("Alamat");
            jTextFieldAlamat.setText(add5);

            String add6 = sqlResult.getString("Status");
            comboBoxStatus.setSelectedItem(add6);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Kesalahan", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}


